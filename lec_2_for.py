'''
for <переменная цикла> in <значение переменной>: # заголовок оператора
    <инструкция_1>
    <инструкция_2>
    <инструкция_3>  # Тело оператора
    ....
    <инструкция_n>

'''

for i in 1, 3, 4:
    print(i ** 2, end=' ')

print()

for i in 1, 3, 4:
    print(i ** 2, end='\n')

for i in 1, 3, 4:
    print(i, i ** 2, sep=' - ')

a = [1, 5, 7, 10]
for i in a:
    print(f'{i}**3 = {i ** 3}')